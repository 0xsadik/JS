
/*

        JavaScript varibales 

        There is 4 ways to declare a javascript variable:
            - using 'var'
            - using 'let'
            - using 'const'
            - using nothing 


        
        What are varibales ?

        variables are containers for storing data (storing data values)
*/


var x = 3;
var y = 6;
var z = x + y;

// another way 

var a, b, c;
a = 10;
